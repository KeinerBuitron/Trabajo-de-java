package herencia;


public class Persona {
    private String nombre;
    private int edad;
    
    //constructor

    public Persona(String nombre, int edad) {
        this.nombre = nombre;
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public int getEdad() {
        return edad;
    }

 
    
}
